package com.cognixia.jump.corejava;

public class Burger {
	private boolean hasCheese;
	private boolean hasLettuce;
	private boolean hasPickles;
	private boolean hasTomatoes;
	private boolean hasOnions;
	private boolean hasMustard;
	private boolean hasKetchup;
	private boolean hasChili;
	
	public Burger(boolean hasCheese, boolean hasLettuce, boolean hasPickles, boolean hasTomatoes, boolean hasOnions, boolean hasMustard, boolean hasKetchup, boolean hasChili) {
		this.hasCheese = hasCheese;
		this.hasLettuce = hasLettuce;
		this.hasPickles = hasPickles;
		this.hasTomatoes = hasTomatoes;
		this.hasOnions = hasOnions;
		this.hasMustard = hasMustard;
		this.hasKetchup = hasKetchup;
		this.hasChili = hasChili;
		
		
			
  }
	public Burger () {
		
	}
	
	
	
	public boolean isHasCheese() {
		return hasCheese;
	}
	public void setHasCheese(boolean hasCheese) {
		this.hasCheese = hasCheese;
	}
	public boolean isHasLettuce() {
		return hasLettuce;
	}
	public void setHasLettuce(boolean hasLettuce) {
		this.hasLettuce = hasLettuce;
	}
	public boolean isHasPickles() {
		return hasPickles;
	}
	public void setHasPickles(boolean hasPickles) {
		this.hasPickles = hasPickles;
	}
	public boolean isHasTomatoes() {
		return hasTomatoes;
	}
	public void setHasTomatoes(boolean hasTomatoes) {
		this.hasTomatoes = hasTomatoes;
	}
	public boolean isHasOnions() {
		return hasOnions;
	}
	public void setHasOnions(boolean hasOnions) {
		this.hasOnions = hasOnions;
	}
	public boolean isHasMustard() {
		return hasMustard;
	}
	public void setHasMustard(boolean hasMustard) {
		this.hasMustard = hasMustard;
	}
	public boolean isHasKetchup() {
		return hasKetchup;
	}
	public void setHasKetchup(boolean hasKetchup) {
		this.hasKetchup = hasKetchup;
	}
	public boolean isHasChili() {
		return hasChili;
	}
	public void setHasChili(boolean hasChili) {
		this.hasChili = hasChili;
	}
	



	

}
